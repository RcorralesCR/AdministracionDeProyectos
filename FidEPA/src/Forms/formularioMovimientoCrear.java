    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int sucursal_destino = (jComboBoxsucursal_destino.getSelectedIndex()+1);
       
        String producto = labelNombreProducto.getText();
        int cantidad = (int)spinCantidad.getValue();
        
        String fecha_envio = ""+txtFechaEnvio.getDate();
        int id_producto = Integer.parseInt(jComboBoxsucursal_Producto.getSelectedItem().toString());
        String[] productoNuevo = EjecutarOperaciones.obtenerProducto(id_producto);
         int sucursal_origen = Integer.parseInt(productoNuevo[5]);

        int cantidadProducto = EjecutarOperaciones.calcularCantidadProducto(id_producto);

        if (cantidad <= cantidadProducto) {
            EjecutarOperaciones.crearMovimiento(sucursal_origen, sucursal_destino, producto, cantidad, fecha_envio);
            int cantidadTotal=cantidadProducto-cantidad;
            EjecutarOperaciones.restarUnidades(cantidadTotal, id_producto);
            //trae los datos del producto

            String nombreProducto = productoNuevo[0];
            float precioUnitario = Float.parseFloat(productoNuevo[2]);
            String descripcion = productoNuevo[3];
            String ubicacion = txtUbicacion.getText();
            String idSucursal = ""+(jComboBoxsucursal_destino.getSelectedIndex()+1);

            EjecutarOperaciones.agregarProducto(nombreProducto, cantidad, precioUnitario, descripcion, ubicacion, ""+idSucursal);
            JOptionPane.showMessageDialog(null, "Movimiento realizado con Ã©xito");
            int cantidadesRestantes = EjecutarOperaciones.calcularCantidadProducto(id_producto);
            if(cantidadesRestantes<=20){
                JOptionPane.showMessageDialog(null, "Hay menos de 20 unidades del producto en la sucursal de Origen.");
            }

        } else {
            JOptionPane.showMessageDialog(null, "No hay suficiente cantidad del producto en la sucursal de origen");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

